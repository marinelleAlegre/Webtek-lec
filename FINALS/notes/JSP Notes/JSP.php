        <h1>Overview</h1>
        <p>JavaServer Pages (JSP) is a technology for developing Webpages that supports dynamic content. This helps developers insert java code in HTML pages by making use of special JSP tags, most of which start with <% and end with %>.A JavaServer Pages component is a type of Java servlet that is designed to fulfill the role of a user interface for a Java web application. Web developers write JSPs as text files that combine HTML or XHTML code, XML elements, and embedded JSP actions and commands.</p>
        <p>Java Server Pages (JSP) is a server-side programming technology that enables the creation of dynamic, platform-independent method for building Web-based applications. JSP have access to the entire family of Java APIs, including the JDBC API to access enterprise databases.</p>
          
        <p>CLIENT REQUEST</p>
        <p>- When a browser requests for a Webpage, it sends a lot of information to the web server. This information cannot be read directly because this information travels as a part of header of HTTP request.</p>
          <p>SERVER RESPONSE</p>
          <p>- When a Web server responds to a HTTP request, the response typically consists of a status line, some response headers, a blank line, and the document.</p>
        <h2>Advantages of JSP</h2>
        <p>Following table lists out the other advantages of using JSP over other technologies :</p>
        <ul>
            <li>Active Server Pages (ASP)</li>
                 <p>  - The advantages of JSP are twofold. First, the dynamic part is written in Java, not Visual Basic or other MS specific language, so it is more powerful and easier to use. Second, it is portable to other operating systems and non-Microsoft Web servers.
          <li>Server-Side Includes (SSI)</li>
                   <p> - SSI is really only intended for simple inclusions, not for "real" programs that use form data, make database connections, and the like.
          <li>JavaScript</li>
                   <p> - JavaScript can generate HTML dynamically on the client but can hardly interact with the web server to perform complex tasks like database access and image processing etc.
                       
